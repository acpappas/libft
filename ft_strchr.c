#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *nptr;

	nptr = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (nptr);
}
