#include <string.h>

char	*ft_strchr(const char *s, int c)
{	
	char *str;
	
	str = NULL;
	while (*s != '\0')
	{
		if (*s == (char)c)
			str = (char *)s;
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (str);
}
